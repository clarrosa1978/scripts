#set -x
#!/usr/bin/ksh
###########################################################################################
#                              DECLARACION DE VARIABLES                                   #
###########################################################################################

Fecha=`date +%d/%m/%Y`
Hora=`date | awk ' { print$4 } '`
Usuario=`whoami`
Nombre=`lsuser -c -a gecos $LOGNAME | grep $LOGNAME | awk -F ":" ' { print $2 } '`

###########################################################################################
#                                      PRINCIPAL                                          #
###########################################################################################

clear
echo
echo "\t********************************************************************************"
printf "\t%-10s %-10s %-24s %-10s %-20s %-1s \n" \
"*"  "Fecha:" $Fecha "Hora:" $Hora "*"
printf "\t%-10s %-10s %-24s %-10s %-20s %-1s \n" \
"*" "Usuario:" "${Usuario}" "Nombre:" "${Nombre}" "*"
echo "\t********************************************************************************"
