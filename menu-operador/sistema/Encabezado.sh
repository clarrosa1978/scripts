#set -x
#!/usr/bin/ksh
###########################################################################################
#                              DECLARACION DE VARIABLES                                   #
###########################################################################################

Fecha=`date +%d/%m/%Y`
Hora=`date | awk ' { print$4 } '`
Usuario=`whoami`
Nombre=`lsuser -c -a gecos  $LOGNAME | grep $LOGNAME | cut -f2 -d: | cut -f1 -d,`

###########################################################################################
#                                      PRINCIPAL                                          #
###########################################################################################

clear
echo
echo "\t*******************************************************************************"
printf "\t%-10s %-10s %-24s %-10s %-19s %-1s \n" \
"*"  "Fecha:" $Fecha "Hora:" $Hora "*"
printf "\t%-10s %-10s %-24s %-10s %-9s %-9s %-1s \n" \
"*" "Usuario:" $Usuario "Nombre:" $Nombre "*"
echo "\t*******************************************************************************"
